export const add = (num1, num2) =>  {
    let sum = num1 + num2
    return `${sum}`
};
export const subtract = (num1, num2) =>  {
    let sum = num1 - num2
    return `${sum}`
};
export const multiply = (num1, num2) =>  {
    let sum = num1 * num2
    return `${sum}`
};
export const divide = (num1, num2) =>  {
    let sum = num1 / num2
    return `${sum}`
};